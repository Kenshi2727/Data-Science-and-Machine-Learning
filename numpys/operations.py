import numpy as np
arr = np.array([1, 2, 3, 4, 5])
print(arr + arr) # adding two arrays
print(arr - arr) # subtracting two arrays
arr=arr+100 # adding a scalar value to an array
print(arr)

(np.sqrt(arr)) # square root of each element of an array
print(np.exp(arr)) # exponential of each element of an array
print(np.sin(arr)) # sin of each element of an array
print(np.cos(arr)) # cos of each element of an array
print(np.log(arr)) # log of each element of an array
print(np.log10(arr)) # log base 10 of each element of an array
print(np.max(arr)) # maximum value of an array
print(np.min(arr)) # minimum value of an array